#include <unistd.h>

int	main(int arc, char **arv)
{
	int	i;
	int	l;

	l = 1;
	while (arc > l)
	{
		i = 0;
		while (arv[l][i] != '\0')
		{
			write (1, &arv[l][i], 1);
			i++;
		}
		write (1, "\n", 1);
		l++;
	}
}
